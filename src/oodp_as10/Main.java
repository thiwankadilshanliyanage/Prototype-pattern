    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int r=jTable1.getSelectedRow();
        if(r==-1){
            JOptionPane.showMessageDialog(Main.this, "Select a camera first", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            idLabel.setText(dtm.getValueAt(r, 0).toString());
            PrototypeCamera pc=pc_map.get(r+1);
            PrototypeCamera cpc=pc.clone();
            pc_map.put(r+2, cpc);
            Vector v=new Vector();
            v.add(r+2);
            v.add(cpc.getBrand());
            v.add(cpc.getModel());
            v.add(cpc.getType());
            v.add(cpc.getSensor());
            v.add(cpc.getImage_sensor());
            v.add(cpc.getVideo_resolution());
            v.add(cpc.getStorage());
            
            if(cpc.isFlash()){
                v.add("Yes");
            }else{
                v.add("No");
            }
            
            if(cpc.isLcd_display()){
                v.add("Yes");
            }else{
                v.add("No");
            }
            
            dtm.addRow(v);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

