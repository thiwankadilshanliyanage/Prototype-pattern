    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String brand=jTextField1.getText();
        String model=jTextField2.getText();
        String type=jTextField3.getText();
        String sensor=jTextField4.getText();
        String image_sensor=jTextField5.getText();
        String video_resolution=jTextField6.getText();
        String storage=jTextField7.getText();
        boolean flash;
        boolean lcd_display;
        
        if(brand.isEmpty()||model.isEmpty()||type.isEmpty()||sensor.isEmpty()||image_sensor.isEmpty()||video_resolution.isEmpty()||storage.isEmpty()){
            JOptionPane.showMessageDialog(Main.this, "Fill all fields");
        }else{
        
        int rc=dtm.getRowCount()+1;
        
        Vector v=new Vector();
        v.add(rc);
        v.add(brand);
        v.add(model);
        v.add(type);
        v.add(sensor);
        v.add(image_sensor);
        v.add(video_resolution);
        v.add(storage);
        
        if (jComboBox1.getSelectedItem().toString().equals("Yes")) {
            flash=true;
            v.add("Yes");
        }else{
            flash=false;
            v.add("No");
        }
        
        if (jComboBox2.getSelectedItem().toString().equals("Yes")) {
            lcd_display=true;
            v.add("Yes");
        }else{
            lcd_display=false;
            v.add("No");
        }
        
        dtm.addRow(v);
        PrototypeCamera pc=new PrototypeCamera(brand, model, type, sensor, image_sensor, video_resolution, storage, flash, lcd_display);
        pc_map.put(rc, pc);
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        idLabel.setText("Not Selected");
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

